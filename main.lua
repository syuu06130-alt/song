-- キー押下 & 離す
local function pressKey(keyStr)
    local kc = KEY_CODES[keyStr]
    if kc then
        VIM:SendKeyEvent(true,  kc, false, game)
        task.delay(0.05, function()
            VIM:SendKeyEvent(false, kc, false, game)
        end)
    end
end

-- ============================================================
--  再生状態管理
-- ============================================================
local isPlaying = false

local function playSong()
    if isPlaying then
        Rayfield:Notify({
            Title = "Libra Heart",
            Content = "すでに再生中です！",
            Duration = 2,
            Image = "rbxassetid://4483345998",
        })
        return
    end

    isPlaying = true

    Rayfield:Notify({
        Title = "♪ Libra Heart",
        Content = "imaizumi - 再生を開始します",
        Duration = 3,
        Image = "rbxassetid://4483345998",
    })

    task.spawn(function()
        local startTime = tick()
        for _, note in ipairs(notes) do
            local waitTime = note.time - (tick() - startTime)
            if waitTime > 0 then
                task.wait(waitTime)
            end
            pressKey(note.key)
        end
        isPlaying = false
        Rayfield:Notify({
            Title = "♪ Libra Heart",
            Content = "再生が終了しました",
            Duration = 3,
            Image = "rbxassetid://4483345998",
        })
    end)
end

